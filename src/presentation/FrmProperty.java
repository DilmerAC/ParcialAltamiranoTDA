    private void pagination() {
        int totalPaginas;

        this.totalRecords = this.CONTROL.total();
        this.totalPerPage = Integer.parseInt((String) cboTotalPorPagina.getSelectedItem());
        totalPaginas = (int) (Math.ceil((double) this.totalRecords / this.totalPerPage));
        if (totalPaginas == 0) {
            totalPaginas = 1;
        }
        cboNumPagina.removeAllItems();

        for (int i = 1; i <= totalPaginas; i++) {
            cboNumPagina.addItem(Integer.toString(i));
        }
        cboNumPagina.setSelectedIndex(0);
    }

    private void listar(String texto, boolean pagination) {
        this.totalPerPage = Integer.parseInt((String) cboTotalPorPagina.getSelectedItem());
        if ((String) cboNumPagina.getSelectedItem() != null) {
            this.pageNumber = Integer.parseInt((String) cboNumPagina.getSelectedItem());
        }

        if (pagination == true) {
            tableList.setModel(this.CONTROL.list(texto, this.totalPerPage, this.pageNumber));
        } else {
            tableList.setModel(this.CONTROL.list(texto, this.totalPerPage, 1));
        }

        TableRowSorter orden = new TableRowSorter(tableList.getModel());
        tableList.setRowSorter(orden);
        lblTotalRegistros.setText("Showing " + this.CONTROL.totalShowed() + " of " + this.CONTROL.total() + " records.");
    }

