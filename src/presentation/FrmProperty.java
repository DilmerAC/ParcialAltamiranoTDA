    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tableList.getSelectedRowCount() == 1) {
            String id = String.valueOf(tableList.getValueAt(tableList.getSelectedRow(), 0));
            String name = String.valueOf(tableList.getValueAt(tableList.getSelectedRow(), 1));
            String state = String.valueOf(tableList.getValueAt(tableList.getSelectedRow(), 3));
            String address = String.valueOf(tableList.getValueAt(tableList.getSelectedRow(), 2));
            String characteristics = String.valueOf(tableList.getValueAt(tableList.getSelectedRow(), 4));
            String rent_price = String.valueOf(tableList.getValueAt(tableList.getSelectedRow(), 5));

            txtId.setText(id);
            txtName.setText(name);
            txtAddress.setText(address);
            txtCharacteristics.setText(characteristics);
            txtRentPrice.setText(rent_price);
            cbxState.setSelectedItem(state);

            tabGeneral.setEnabledAt(0, false);
            tabGeneral.setEnabledAt(1, true);
            tabGeneral.setSelectedIndex(1);
            this.accion = "edit";
            btnSave.setText("Edit");
        } else {
            this.mensajeError("Seleccione 1 registro a edit.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

