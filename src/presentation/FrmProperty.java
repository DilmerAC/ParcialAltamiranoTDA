    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtName.getText().length() == 0 || txtName.getText().length() > 100) {
            JOptionPane.showMessageDialog(this, "You should enter a name with a length of 100 characters as max, es obligatory.", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtName.requestFocus();
            return;
        }
        if (txtRentPrice.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "You should specify the rent price, is obligatory.", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtRentPrice.requestFocus();
            return;
        }
        if (txtAddress.getText().length() == 0) {
            JOptionPane.showMessageDialog(this, "Debes specify the address, is obligatory.", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtAddress.requestFocus();
            return;
        }
        if (txtCharacteristics.getText().length() > 255) {
            JOptionPane.showMessageDialog(this, "You should specify the characteristics with a maximum length of 255 characters.", "Sistema", JOptionPane.WARNING_MESSAGE);
            txtCharacteristics.requestFocus();
            return;
        }
        String resp;
        if (this.accion.equals("edit")) {
            //Update
            //int id, String name, String address, String state, String characteristics, Double rent_price
            String state = cbxState.getSelectedItem().toString();
            resp = this.CONTROL.update(Integer.parseInt(txtId.getText()), txtName.getText(), txtAddress.getText(), state, txtCharacteristics.getText(), Double.parseDouble(txtRentPrice.getText()));
            if (resp.equals("OK")) {
                this.mensajeOk("Successfully Updated!");
                this.limpiar();
                this.listar("", false);
                tabGeneral.setSelectedIndex(0);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setEnabledAt(0, true);
            } else {
                this.mensajeError(resp);
            }
        } else {
            //save
            String state = cbxState.getSelectedItem().toString();
            resp = this.CONTROL.insert(txtName.getText(), txtAddress.getText(), state, txtCharacteristics.getText(), Double.parseDouble(txtRentPrice.getText()));
            if (resp.equals("OK")) {

                this.mensajeOk("Successfully Registered!");
                this.limpiar();
                this.listar("", false);
                /*tabGeneral.setSelectedIndex(0);
                tabGeneral.setEnabledAt(1, false);
                tabGeneral.setEnabledAt(0, true);*/
            } else {
                this.mensajeError(resp);
            }
        }

    }//GEN-LAST:event_btnSaveActionPerformed


